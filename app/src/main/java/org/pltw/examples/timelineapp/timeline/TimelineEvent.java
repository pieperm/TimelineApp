package org.pltw.examples.timelineapp.timeline;

import java.util.Date;

/**
 * Created by pieperm on 3/14/17.
 */

public class TimelineEvent {

    private String title, description;
    private Date startDate, endDate;

    public TimelineEvent(String title, String description, Date startDate, Date endDate) {
        this.title = title;
        this.description = description;
        this.startDate = startDate;
        this.endDate = endDate;
    }

    public TimelineEvent() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    // TODO: add a toString method using StringBuilder

}
